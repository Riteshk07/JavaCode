class A {
    public static void main(String[] args) {
        System.out.println(Boolean.toString(true));
        System.out.println(Character.toString('A'));
        byte a = 23;
        System.out.println(Byte.toString(a));
        short b = 345;
        System.out.println(Short.toString(b));
        System.out.println(Integer.toString(78906));
        System.out.println(Long.toString(7890678789787878789L));
        System.out.println(Float.toString(2.3f));
        System.out.println(Double.toString(4.567));
    }
}