class A {
    //primitive type variables ... not initialized with explicit initial value..
    //will be initialized with default value...
    static boolean a;
    static char b;
    static byte c;
    static short d;
    static int e;
    static long f;
    static float g;
    static double h;
    static String i;
    static int[] j;

    public static void main(String[] args) {        
        
        System.out.println(a);
        System.out.println(">"+b+"<");
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
        System.out.println(j);
    }
}